Auch bei rechtzeitigem Zahlungseingang kann kein Anspruch auf Ausstellung eines Tickets geltend gemacht werden, wenn das maximale Ticketkontingent überschritten wurde.

 </div><div class="row"> 6. Rückerstattung bei Nichtverfügbarkeit<br />

Sollte eine Ticketbestellung aufgrund der Ausschöpfung des maximalen Kontingents von 300 Tickets nicht mehr berücksichtigt werden können, wird der bereits bezahlte 
Ticketpreis vollständig und unverzüglich an den Kunden rückerstattet.

Weitere Ansprüche, insbesondere Schadenersatzansprüche, sind ausgeschlossen, sofern keine zwingenden gesetzlichen Vorschriften entgegenstehen.

