 </div><div class="row"> 2. Vertragspartner<br />

Der Kaufvertrag kommt zustande zwischen dem Besteller der Tickets (nachfolgend „Kunde“) und dem Veranstalter der Veranstaltung „Final3“ (nachfolgend „Veranstalter“).

 </div><div class="row"> 3. Vertragsabschluss<br />

Die Darstellung der Tickets im Online-Anmeldeformular stellt kein rechtlich bindendes Angebot dar, sondern eine unverbindliche Aufforderung zur Abgabe einer Bestellung.
Mit dem Absenden der Ticketbestellung gibt der Kunde ein verbindliches Angebot zum Abschluss eines Kaufvertrages ab. Der Vertrag kommt erst durch eine ausdrückliche Bestätigung des Veranstalters oder durch Zusendung der Ticketbestätigung zustande.

