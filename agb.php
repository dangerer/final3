Die Gesamtanzahl der für die Veranstaltung verfügbaren Plätze ist auf 300 Tickets begrenzt.

Der Veranstalter ist berechtigt, Ticketbestellungen auch nach Zahlungseingang abzulehnen, sofern das maximale Kontingent von 300 Tickets bereits ausgeschöpft ist.

In diesem Fall kommt kein wirksamer Ticketvertrag zustande.

 </div><div class="row"> 5. Zahlung und Ticketgültigkeit<br />

Die Tickets sind <span class="bold">erst nach vollständigem Zahlungseingang</span> gültig.

